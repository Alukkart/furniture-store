export function translateAdminRole(locale: SiteLocale, role: string) {
  if (locale === "ru") {
    const labels: Record<string, string> = {
      Administrator: "Администратор",
      Manager: "Менеджер",
      Warehouse: "Склад",
      Executive: "Руководитель",
      Client: "Клиент",
    };
    return labels[role] ?? role;
  }

  return role;
}

export function translateOrderStatus(locale: SiteLocale, status: string) {
  if (locale === "ru") {
    const labels: Record<string, string> = {
      pending: "В ожидании",
      processing: "В обработке",
      shipped: "Отгружен",
      delivered: "Доставлен",
      cancelled: "Отменен",
    };
    return labels[status] ?? status;
  }

  return status.charAt(0).toUpperCase() + status.slice(1);
}

export function translateAuditRelative(locale: SiteLocale, diffMs: number) {
  const mins = Math.floor(diffMs / 60000);
  const hours = Math.floor(diffMs / 3600000);
  const days = Math.floor(diffMs / 86400000);
  const t = adminText[locale].audit;

  if (mins < 1) return t.justNow;
  if (mins < 60) return `${mins}${t.agoM}`;
  if (hours < 24) return `${hours}${t.agoH}`;
  return `${days}${t.agoD}`;
}
